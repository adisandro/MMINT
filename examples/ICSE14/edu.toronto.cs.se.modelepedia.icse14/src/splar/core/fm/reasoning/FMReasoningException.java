package splar.core.fm.reasoning;

public class FMReasoningException extends Exception {

	public FMReasoningException() {
		super();
	}

	public FMReasoningException(String message) {
		super(message);
	}

	public FMReasoningException(Throwable throwable) {
		super(throwable);
	}

	public FMReasoningException(String message, Throwable throwable) {
		super(message, throwable);
	}

}
